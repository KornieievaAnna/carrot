// У змінній min лежить число від 0 до 59.
// Визначте, в яку чверть години потрапляє
// це число(у першу, другу, третю чи четверту).

const min = 26;

if (min <= 15) {
    alert('Перша чверть.');
} else if (15 < min && min <= 30) {
    alert('Друга чверть.');
} else if (30 < min && min <= 45) {
    alert('Третя чверть.');
} else alert('Четверта чверть.')
